public class Case {
    private final int posx;
    private final char posy;

    public Case(int posx, char posy) {
        this.posx = posx;
        this.posy = posy;
    }

    public int getPosx() {
        return posx;
    }

    public char getPosy() {
        return posy;
    }

    @Override
    public String toString() {
        return "Case{"+ posx +", " + posy +
                '}';
    }
}
